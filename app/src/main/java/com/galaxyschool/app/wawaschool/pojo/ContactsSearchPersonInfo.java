package com.galaxyschool.app.wawaschool.pojo;

public class ContactsSearchPersonInfo {

	private String Id;
	private boolean IsFriend;
	private String HeadPicUrl;
	private String NewFriendId;
	private String NewFriendName;
	private String NewFriendNickName;

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public boolean isIsFriend() {
		return IsFriend;
	}

	public void setIsFriend(boolean isFriend) {
		IsFriend = isFriend;
	}

	public String getHeadPicUrl() {
		return HeadPicUrl;
	}

	public void setHeadPicUrl(String headPicUrl) {
		HeadPicUrl = headPicUrl;
	}

	public String getNewFriendId() {
		return NewFriendId;
	}

	public void setNewFriendId(String newFriendId) {
		NewFriendId = newFriendId;
	}

	public String getNewFriendName() {
		return NewFriendName;
	}

	public void setNewFriendName(String newFriendName) {
		NewFriendName = newFriendName;
	}

	public String getNewFriendNickName() {
		return NewFriendNickName;
	}

	public void setNewFriendNickName(String newFriendNickName) {
		NewFriendNickName = newFriendNickName;
	}

}
